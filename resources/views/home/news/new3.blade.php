      <h2 class="am-article-title blog-title" style="font-weight: 700;font-size:25px;">
        小小盲盒背后的千万级市场：30万闲鱼玩家，月发布量增长320%
      </h2>
      <h4 class="am-article-meta blog-meta">2019-08-08 17:13:50</h4>

      <div class="am-g blog-content">
        <div class="am-u-sm-12">
            <img src="/h/images/O1CN01211wz02NQoZX60CbY_!!0-martrix_bbs.jpg" style="width:300px;height:450px;margin-left:200px;" data-spm-anchor-id="a210m.7696189.0.i0.782d6003e1CJjL">
            <p>盲盒，一种潮流玩具，里面装的是一款表情呆萌、设计时尚的人偶玩具，但购买时没有人知道里面装的是哪一款。有人说：“人生就像一盒各式各样的巧克力，你永远不知道下一块将会是什么口味，除了人生，盲盒也是这样。”
              因为这种未知性，盲盒才这样令人欲罢不能，因此，年轻盲盒爱好者收藏之余，交换和转让成为他们的另一大诉求，闲鱼上的盲盒交易随之火爆。如今闲鱼上的盲盒交易已经是一个千万级的市场，过去一年闲鱼上有30万盲盒玩家进行交易，
              每月发布闲置盲盒数量较一年前增长320%，最受追捧的盲盒价格狂涨39倍，盲盒已然成为年轻爱好者的一种社交货币。</p>   
            <p>&nbsp;</p>
            <p>盲盒爱好者是水浒卡爱好者的25倍， 盲盒走红的密码是什么？</p>
            <p>&nbsp;</p>
            <p>盲盒的概念并不新鲜，80后童年疯狂收集的小浣熊水浒卡就是盲盒的初代形式。为了一张宋江卡，有熊孩子把一整箱一整箱泡面往家里搬。而现在，为了获得一个稀有的隐藏款盲盒，更多成年人不惜在闲鱼上溢价几十倍来购买。
              而数据显示，盲盒的受众群体比水浒卡更为宽广。闲鱼上的盲盒爱好者是水浒卡爱好者的25倍。而这两大人群存在差别，闲鱼上的盲盒爱好者年龄在18-35岁，以女生为主；而在闲鱼上痴迷于水浒卡的玩家主要集中在25-35岁，以男性为主。
              相比水浒卡，盲盒囊括80、90后的大年龄跨度受众群，加上女性消费群体的加入，让盲盒市场看到更大的潜力。</p>
            <p>&nbsp;</p>
            <p>盲盒让人欲罢不能的原理是什么？分析认为，第一、盲盒作品来是大众易于接受的可爱形象，从来没了解过盲盒的路人看到也会感兴趣，加之各个设计师都有自己的粉丝基础来买单，受众群体广。第二、盲盒的价格亲民，
              几十元是绝大多数人都能承受的价格。第三、盲盒有固定款和隐藏款之分，而且有的隐藏款出现的概率极低，隐藏款这个设定很好地抓住了消费者的心理，大家都想试试自己的运气，一旦抽中隐藏款，在闲鱼上的交易价就能翻上好几十倍。</p>
            <p>&nbsp;</p>
            <p>闲鱼上最受追捧的盲盒狂涨39倍，最热门盲盒还属泡泡玛特</p>
            <p>&nbsp;</p>
            <p>在盲盒界，谁才是大热门？是新近网红Molly，还是老牌sonyangle?数据显示，闲鱼上交易最热门的十大盲盒产品分别是：Molly、独角兽、L.O.L、sonyangle、毕奇(pucky) 、littleamber、吾皇万睡、actoys猫铃铛、lego迷你人仔、sml盲盒。
              泡泡玛特出品的Molly成为名副其实的盲盒代表，过去一年，闲鱼上的molly 娃娃交易超过23万单，均价270元。</p>
            <p>&nbsp;</p>
            <p>而细数闲鱼上涨价迅猛的热门盲盒，第一名要数泡泡玛特的潘神圣诞隐藏款，原价59元，现在在闲鱼已经卖到2350元的高价，狂涨39倍。另一款泡泡玛特的molly胡桃夹子王子隐藏款涨幅也很高，原价59元，现在闲鱼均价1350元，涨幅22倍。
              另外，labubu的宇航员隐藏款原价699，在闲鱼的价格已经涨到3000元，涨幅3.3倍。</p>
              <p>&nbsp;</p>
            <p>盲盒爱好者主要集中在上海、北京、广州、天津、杭州，一位上海闲鱼用户转卖盲盒一年就赚10万元之多。</p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight: 700;font-size:16px;">盲盒成年轻人的社交货币，闲鱼盲盒鱼塘人气高高涨</strong>
            </p>
            <p>&nbsp;</p>
            <p>随着闲鱼上的盲盒交易增长，盲盒俨然成为了年轻人的新型社交货币，换娃、改娃的圈子不断崛起，在闲鱼应运而生许多鱼塘，泡泡玛特鱼塘就是其中之一，盲盒达人“王没谱”于四前建立了这个鱼塘，前身叫Molly鱼塘，
              最初建立的原因是他无意中在微博上看到小红帽和大灰狼的可动关节玩偶觉得很可爱，从此就“入坑”了Molly，他建起了鱼塘希望可以和更多的藏友互通有无。王没谱认为：“收藏最大的乐趣不光是占有收集，还有通过收藏认识志同道合的朋友，
              转让过程中聊起越聊越投缘，相见恨晚的有很多，同城的更是很多见面熟悉之后一起参加各种展会和活动，这种突然多出一个至交好友的感觉非常奇妙。”</p>
            <p>&nbsp;</p>
            <p>闲鱼鱼塘负责人沈婷表示：“盲盒鱼塘的兴起，是年轻人基于兴趣形成的圈子文化，承载着年轻人对于艺术审美的追求。我们一直认为不能小瞧了兴趣的力量，我们会不断推进基于兴趣的鱼塘培养，通过扶持塘主以及吸引明星加入来提升社区的活跃度和知名度。
              兴趣社区也是当前闲鱼聚焦的层面。”</p>
