        东北大米征服半个中国 占天猫超市大米销量6成
      </h2>
      <h4 class="am-article-meta blog-meta">2019-07-31 15:40:50</h4>

      <div class="am-g blog-content">
        <div class="am-u-sm-12">
            <img src="/h/images/O1CN01hOOCze2NQoZXoJXxM_!!2-martrix_bbs.png" style="width:400px;height:400px;margin-left:200px;" data-spm-anchor-id="a210m.7696189.0.i0.782d6003e1CJjL">
            <p>近日，2名男子坐火车时因争论谁更了解东北大米而大打出手，还创造了微博热搜的新话题。究竟消费者在选大米这件事上，是怎么用胃来投票的呢？来自天猫超市的数据显示，东北大米凭借高达60%的销量占比，成功征服了半个中国。</p>   
            <p>我国有东北、苏北和岭南三个大米主产区。其中苏北米主要是圆粒，岭南米则主要是长粒。东北黑土地包容的特性在种植水稻这件事上展现出来：既产长粒米，又产圆粒米，而广为人知的东北五常大米则属于中长粒，介于长粒和圆粒之间。</p>
            <p>过去，消费者对大米的选择受到产地影响较大。上世纪八九十年代，消费者主要在本地粮油店购买散称大米，这些大米基本都是从最近的产地运输过来的。2000年起，连锁超市、大卖场逐渐成为消费者购买包括大米在内的日用品的主要场所，大米也有了品牌和标准化的包装。</p>
            <p>而从2010年左右开始，随着电商市场的快速崛起，天猫超市等线上平台逐渐成为一二线城市消费者购买大米的主要渠道。由于线上平台可供选择的种类丰富，且能送货上门免去搬运之苦，“买米”这件事变得更为轻松。与此同时，东北米、苏北米和岭南米也走出产地周边，开始销往全国各地。“从产地生产，到送至消费者手中，最短的只需要5天时间。”天猫超市相关负责人告诉记者。</p>
            <p>盛名在外的东北大米得到了全国消费者的青睐。天猫超市数百种不同品牌和规格的大米中，东北大米销量占比达60%。东北、华北和华东地区的消费者“吃饭”首选都是东北米。岭南米紧跟其后，占领了华南、西南地区消费者的饭桌。而苏北米的消费者则主要集中在华中地区。</p>
            <p>配送的便利性和商品的丰富性，也促进了大米消费的升级。今年上半年天猫超市3元至5元一斤的中档米销售额增速达到近70%，远高于3元以下一斤的大米。而10元以上一斤的高端米销售额增速也达到近30%。在“吃饭”这件事上，消费者正变得越来越认真。大米消费升级的趋势，也让国外的米企看到机会，日本、泰国、柬埔寨等国的大米相继进入中国市场，开始对中高端市场发起冲击。</p>
            
