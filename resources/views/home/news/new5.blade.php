      <h2 class="am-article-title blog-title" style="font-weight: 700;font-size:25px;">
        注意：通过QQ群盗取买家快递信息的风险警示
      </h2>
      <h4 class="am-article-meta blog-meta">2019-05-20 12:28:30</h4>

      <div class="am-g blog-content">
        <div class="am-u-sm-12">
            <p>
              <strong style="font-weight:700;font-size:16px;">各位卖家：</strong>
            </p>
            <p>&nbsp;</p>
            <p>近期，出现多起黑产团伙潜入卖家与物流公司创建的查件QQ群盗取买家快递信息现象请各位卖家注意防范和规避类似风险！</p>   
            <p>&nbsp;</p>
            <p>在日常运营中，一些卖家会收到买家投诉快递破损、丢件、催单、退回、修改地址等异常快递处理的需求，因此卖家会与物流公司建立查件QQ群，核准异常快递的相关信息。</p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight:700;font-size:16px;">由于这类QQ群通常以“查件、查单”等常用关键词作为群名，且没有严格的进群验证审核，使得黑产团伙能轻松检索群名加入其中，并趁机从中盗取异常快递信息，用于实施诈骗。</strong>
            </p>
            <p>&nbsp;</p>
            <p>基于盗取的快递信息，诈骗团伙会伪装成快递公司客服人员给信息被盗的买家打电话，以快递有问题需办理退款等理由，引导买家打开钓鱼链接填写银行账户密码、或通过互联网借贷产品贷款、扫描支付二维码等操作，骗取钱款。</p>
            <p>&nbsp;</p>
            <p>根据新《网络安全法》要求，卖家在处理买家个人信息时，有责任和义务采取必要措施确保买家个人信息安全，防止泄露。在发生或者可能发生买家个人信息泄露的情况时，应当立即采取补救或风险控制措施。</p>
            <p>&nbsp;</p>
            <p>阿里巴巴高度重视用户数据和个人信息安全，我们警示和建议卖家，需及时排查此类通过QQ群发送买家快递信息的风险。</p>
            <p>&nbsp;</p>
            <p>否则，对于卖家因自身经营管理存在漏洞而导致买家个人信息泄露的，我们将实施扣分处理，情节严重的会直接做关店处置。</p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight:700;font-size:16px;">应对此类黑产人员潜伏进卖家和物流公司沟通QQ群、盗取异常快递信息的风险，所有平台上的卖家有必要做好几方面工作：</strong>
            </p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight:700;font-size:16px;">（一）建议不使用非本网络交易平台提供的即时通信工具交流含有买家个人信息的内容。</strong>
            </p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight:700;font-size:16px;">（二）建议对即时通信工具的信息接收方进行严格的准入验证和身份审核，对建立群组交流的，应当定期对群内人员做好身份复核和清理工作。</strong>
            </p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight:700;font-size:16px;">（三）应避免在没有核准身份的即时通信工具群组(例如QQ群组)内直接发送涉及买家个人敏感信息的内容。涉及收件人姓名、手机号、收货地址等买家敏感信息,请务必单独发送给物流公司对应工作人员。</strong>
            </p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight:700;font-size:16px;">（四）要求各物流公司网点必须履行物流快递信息保密义务，并在将快递信息发送到各网点时，敦促网点工作人员避免在即时通信工具群组(例如QQ 群组)内发送快递信息。</strong>
            </p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight:700;font-size:16px;">FAQ</strong>
            </p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight:700;font-size:16px;">Q: 阿里巴巴对泄露买家信息的行为，具体处罚规则是什么？</strong>
            </p>
            <p>&nbsp;</p>
            <p>A：淘宝网规则第五十三条对卖家泄露买家信息的处罚规则作出了解释：“泄露他人信息，是指未经允许发布、传递他人隐私信息，涉嫌侵犯他人隐私权的行为。泄露他人信息的，淘宝对会员所泄露的他人隐私资料的信息进行删除，每次扣6分；情节严重的，每次扣48分。</p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight:700;font-size:16px;">Q:我在淘宝上开店只能用千牛、旺旺、钉钉这个几个阿里官方工具跟物流公司沟通吗？</strong>
            </p>
            <p>&nbsp;</p>
            <p>A:为确保信息安全，建议卖家用平台提供的即时通信工具（千牛、旺旺、钉钉）和物流公司沟通，不建议用QQ群等外部即时通信工具。</p>
            <p>&nbsp;</p>
            <p>
              <strong style="font-weight:700;font-size:16px;">Q：如果物流公司要求用QQ沟通怎么办，会被处罚吗？</strong>
            </p>
            <p>&nbsp;</p>
            <p>A：卖家使用QQ群沟通工作不会被处罚，但是卖家在QQ群里张贴消费者敏感信息（如手机号、姓名、收货地址等）且被诈骗团伙盗取用于欺诈等违法活动，按照淘宝规则阿里平台有权给予卖家处罚。</p>
        
